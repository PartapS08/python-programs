# Relational Operators are ==, !=, >, <, >=, <=. Check Readme.md for more details.
# Relational operators are used to compare two values and return a boolean value (True or False).
# Values can be numbers, strings, or any other data type.
# Mostly used in conditional statements like if, if-else.

##### Equal to (==) Operator Example #####

a = 10
b = 20
result = a == b
print("a == b is", result)
# Output:
# a == b is False

a = 10
b = 10
if a == b:
    print("a and b are equal")
# Output:
# a and b are equal

##### Not Equal to (!=) Operator Example #####

a = 10
b = 20
result = a != b
print("a != b is", result)
# Output:
# a != b is True

##### Greater than (>) Operator Example #####

a = 10
b = 20
result = a > b
print("a > b is", result)
# Output:
# a > b is False

##### Less than (<) Operator Example #####

a = 10
b = 20
result = a < b
print("a < b is", result)
# Output:
# a < b is True

##### Greater than or Equal to (>=) Operator Example #####

a = 10
b = 20
result = a >= b
print("a >= b is", result)
# Output:
# a >= b is False

##### Less than or Equal to (<=) Operator Example #####

a = 10
b = 20
result = a <= b
print("a <= b is", result)
# Output:
# a <= b is True

